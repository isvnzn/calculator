const add = (aOperand, bOperand) => {
  return aOperand + bOperand;
};

const subtract = (aOperand, bOperand) => {
  return aOperand - bOperand;
};

const multiply = (aOperand, bOperand) => {
  return aOperand * bOperand;
};

const divide = (aOperand, bOperand) => {
  return aOperand / bOperand;
};
